package enums;

/**
 * Each mode represents a type of gamemode
 *
 * Mode is set in repective listeners.
 */
public enum Mode
{
    /**
     * PlayerVsPlayer
     */
    PVP,
    /**
     * PlayerVsAI
     */
    PVAI,
    /**
     * AIVsAI
     */
    AIVAI,
    /**
     * Pause game
     */
    PAUSE
}
